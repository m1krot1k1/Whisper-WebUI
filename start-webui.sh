    echo "‚ùå –û—à–∏–±–∫–∞ –∑–∞–ø—É—Å–∫–∞ API —Å–µ—Ä–≤–µ—Ä–∞"
    exit 1
fi

# –ó–∞–ø—É—Å–∫–∞–µ–º –æ—Å–Ω–æ–≤–Ω–æ–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –Ω–∞ 0.0.0.0 —Å –ø–æ—Ä—Ç–æ–º 7860
echo "–ó–∞–ø—É—Å–∫ WebUI –Ω–∞ –ø–æ—Ä—Ç—É 7860..."
python app.py --server_name 0.0.0.0 --server_port 7860 --username "$WHISPER_USERNAME" --password "$WHISPER_PASSWORD" --theme "$WHISPER_THEME" &
